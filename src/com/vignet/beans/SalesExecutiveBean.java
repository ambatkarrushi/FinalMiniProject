package com.vignet.beans;

public class SalesExecutiveBean {

	private String vinNumber;
	private String carManufacturer;
	private String carModelName;
	private String varient;
	private double price;
	private String availabeStatus;

	public String getVinNumber() {
		return vinNumber;
	}

	public void setVinNumber(String vinNumber) {
		this.vinNumber = vinNumber;
	}

	public String getCarManufacturer() {
		return carManufacturer;
	}

	public void setCarManufacturer(String carManufacturer) {
		this.carManufacturer = carManufacturer;
	}

	public String getCarModelName() {
		return carModelName;
	}

	public void setCarModelName(String carModelName) {
		this.carModelName = carModelName;
	}

	public String getVarient() {
		return varient;
	}

	public void setVarient(String varient) {
		this.varient = varient;
	}

	public double getPrice() {
		return price;
	}

	public void setPrice(double price) {
		this.price = price;
	}

	public String getAvailabeStatus() {
		return availabeStatus;
	}

	public void setAvailabeStatus(String availabeStatus) {
		this.availabeStatus = availabeStatus;
	}
}
